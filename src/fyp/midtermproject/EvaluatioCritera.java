    private void DeleteBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteBTNActionPerformed
        // TODO add your handling code here:
         DefaultTableModel model= (DefaultTableModel) jTable1.getModel();
        if(jTable1.getSelectedRowCount()==1)
        {
            model.removeRow(jTable1.getSelectedRow());
        }
        else
        {
            if(jTable1.getRowCount()==0)
            {
            JOptionPane.showMessageDialog(null,"Table is empty");
        }
        else{
           JOptionPane.showMessageDialog(null,"Please select any row!");
        }
        }
    }//GEN-LAST:event_DeleteBTNActionPerformed

    private void UpdateBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateBTNActionPerformed
        // TODO add your handling code here:
         DefaultTableModel model1= (DefaultTableModel) jTable1.getModel();
        if(jTable1.getSelectedRowCount()==1){
              String EvlTitle = Title.getText();
              String EvlMarks = Marks.getText();
              String EvlRubrics = Rubrics.getText();
              String EvlDaeadline = Deadline.getText();
                 
        jTable1.setValueAt(EvlTitle , jTable1.getSelectedRow(), 0);
        jTable1.setValueAt(EvlMarks, jTable1.getSelectedRow(), 1);
        jTable1.setValueAt(EvlRubrics, jTable1.getSelectedRow(), 2);
        jTable1.setValueAt(EvlDaeadline, jTable1.getSelectedRow(), 3);
        
        JOptionPane.showMessageDialog(null," Updated Successfully!");
        }
        else{
        if(jTable1.getRowCount()==0){
            JOptionPane.showMessageDialog(null,"Table is empty");
        }
        else{
            JOptionPane.showMessageDialog(null,"Please select any row!");
        }
        }
    }//GEN-LAST:event_UpdateBTNActionPerformed

    private void SearchFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_SearchFieldKeyReleased
        // TODO add your handling code here:
         DefaultTableModel model1 = (DefaultTableModel) jTable1.getModel();
        String search = SearchField.getText();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(model1);
        jTable1.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(search));
    }//GEN-LAST:event_SearchFieldKeyReleased

