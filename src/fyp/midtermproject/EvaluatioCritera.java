    public ArrayList ListEvlCriteria(String EvlTitle, String EvlMarks, String EvlRubrics, String EvlDaeadline)
    {
        ArrayList<EvaluationData> list = new ArrayList<>() ;
        EvaluationData Ed = new EvaluationData(EvlTitle, EvlMarks, EvlRubrics, EvlDaeadline);
        list.add(Ed);
       
        return list;
      }
  
     public void addRow(String EvlTitle, String EvlMarks, String EvlRubrics, String EvlDaeadline)
    {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
         ArrayList<EvaluationData> list = ListEvlCriteria( EvlTitle, EvlMarks, EvlRubrics, EvlDaeadline);
         Object rowData[] = new Object[4];
         for (int i =0 ;i < list.size(); i++)
         {
             rowData[0] = list.get(i).EvlTitle;
             rowData[1] = list.get(i).EvlMarks;
             rowData[2] = list.get(i).EvlRubrics;
             rowData[3] = list.get(i).EvlDaeadline;
             model.addRow(rowData);
         }
       
      }

